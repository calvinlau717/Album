import UIKit


// Dark mode reference color
// https://uxdesign.cc/dark-mode-ui-design-the-definitive-guide-part-1-color-53dcfaea5129

extension UIColor {
    static var primaryText: UIColor {
        return UIColor(rgb: 0xF5F5F5)
    }
    
    static var descText: UIColor {
        return UIColor(rgb: 0xE0E0E0)
    }
    
    static var darkBackground: UIColor {
        return UIColor(rgb: 0x212121)
    }
    
    static var primaryRed: UIColor {
        return UIColor(rgb: 0xFF7452)
    }
}

